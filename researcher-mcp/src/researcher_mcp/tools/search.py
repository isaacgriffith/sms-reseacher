from researcher_mcp.core.registry import get_registry
from researcher_mcp.sources.base import PaperRecord
from researcher_mcp.sources.crossref import CrossRefSource
from researcher_mcp.sources.open_alex import OpenAlexSource
from researcher_mcp.sources.semantic_scholar import SemanticScholarSource

mcp: FastMCP = FastMCP("researcher-mcp")

# ---------------------------------------------------------------------------
# Shared response schemas
# ---------------------------------------------------------------------------


class SourceFailure(BaseModel):
    """Record of a database source that failed during fan-out search.

    Attributes:
        source: Database index identifier (e.g. ``"ieee_xplore"``).
        reason: Short reason string (e.g. ``"rate_limited"``, ``"unreachable"``).

    """

    source: str
    reason: str


class SearchPapersResult(BaseModel):
    """Result of a fan-out multi-database paper search.

    Attributes:
        papers: Deduplicated list of :class:`PaperRecord` objects.
        total_found: Number of unique papers found before truncation.
        sources_queried: List of database index identifiers that were queried.
        sources_failed: List of sources that raised exceptions during the search.
        truncated: True when ``total_found`` exceeds ``max_results``.

    """

    papers: list[PaperRecord]
    total_found: int
    sources_queried: list[str]
    sources_failed: list[SourceFailure]
    truncated: bool


class SemanticScholarPaperDetail(PaperRecord):
    """Extended paper record with Semantic Scholar-specific fields.

    Attributes:
        tldr: One-sentence summary generated by the Semantic Scholar model.
        influential_citation_count: Number of influential citations.
        is_open_access: Whether the paper is openly accessible.
        fields_of_study: List of academic fields the paper belongs to.

    """

    tldr: str | None = None
    influential_citation_count: int = 0
    is_open_access: bool = False
    fields_of_study: list[str] = []


# ---------------------------------------------------------------------------
# Legacy singletons for get_paper (DOI-cascade via CrossRef → SS → OA)
# ---------------------------------------------------------------------------

_legacy_client: httpx.AsyncClient | None = None
_legacy_ss: SemanticScholarSource | None = None
_legacy_oa: OpenAlexSource | None = None
_legacy_cr: CrossRefSource | None = None


def _get_legacy_sources() -> tuple[SemanticScholarSource, OpenAlexSource, CrossRefSource]:
    """Return (lazily created) legacy source singletons for get_paper.

    Returns:
        Tuple of (SemanticScholarSource, OpenAlexSource, CrossRefSource).

    """
    global _legacy_client, _legacy_ss, _legacy_oa, _legacy_cr
    if _legacy_client is None:
        settings = get_settings()
        _legacy_client = make_retry_client()
        _legacy_ss = SemanticScholarSource(_legacy_client, rpm=settings.semantic_scholar_rpm)
        _legacy_oa = OpenAlexSource(_legacy_client, rpm=settings.open_alex_rpm)
        _legacy_cr = CrossRefSource(_legacy_client)
    assert _legacy_ss is not None and _legacy_oa is not None and _legacy_cr is not None
    return _legacy_ss, _legacy_oa, _legacy_cr


# ---------------------------------------------------------------------------
# Fan-out helper
# ---------------------------------------------------------------------------


async def _search_one(
    index: str,
    source: Any,
    query: str,
    max_results: int,
    year_from: int | None,
    year_to: int | None,
) -> tuple[str, list[PaperRecord] | None, str | None]:
    """Search a single source, returning (index, papers_or_None, error_or_None).

    Args:
        index: Database index identifier.
        source: DatabaseSource instance.
        query: Search query string.
        max_results: Maximum results to request.
        year_from: Earliest publication year filter.
        year_to: Latest publication year filter.

    Returns:
        Tuple of ``(index, results, error_reason)`` — exactly one of
        ``results`` or ``error_reason`` will be non-None.

    """
    try:
        results = await source.search(
            query, max_results=max_results, year_from=year_from, year_to=year_to
        )
        return (index, results, None)
    except Exception as exc:  # noqa: BLE001
        reason = _classify_error(exc)
        return (index, None, reason)


def _classify_error(exc: Exception) -> str:
    """Convert an exception into a short reason string.

    Args:
        exc: The exception that was raised by a source.

    Returns:
        A short classification string suitable for :class:`SourceFailure.reason`.

    """
    msg = str(exc).lower()
    if "401" in msg or "403" in msg or "auth" in msg or "unauthorized" in msg:
        return "auth_failed"
    if "429" in msg or "rate" in msg:
        return "rate_limited"
    if "timeout" in msg or "connect" in msg or "transport" in msg:
        return "unreachable"
    return "unreachable"


# ---------------------------------------------------------------------------
# Upgraded fan-out search_papers (T025)
# ---------------------------------------------------------------------------


@mcp.tool()
async def search_papers(
    query: str,
    indices: list[str] | None = None,
    max_results: int = 100,
    year_from: int | None = None,
    year_to: int | None = None,
) -> SearchPapersResult:
    """Fan-out search across all configured academic database indices.

    Queries all enabled sources concurrently via :func:`asyncio.gather`.
    Results are merged and deduplicated (DOI primary key; normalised
    title+first-author fallback).  Sources that fail are recorded in
    ``sources_failed`` but do not abort the overall search.

    Args:
        query: Free-text or boolean search query.
        indices: Optional list of :class:`DatabaseIndex` values to query.
            Pass ``None`` to query all registered sources.
        max_results: Maximum total papers to return after deduplication.
        year_from: Earliest publication year filter (inclusive).
        year_to: Latest publication year filter (inclusive).

    Returns:
        A :class:`SearchPapersResult` with merged, deduplicated papers and
        per-source failure records.

    """
    registry = get_registry()
    enabled = registry.get_enabled(indices)

    tasks = [_search_one(idx, src, query, max_results, year_from, year_to) for idx, src in enabled]
    raw_results = await asyncio.gather(*tasks)

    all_papers: list[PaperRecord] = []
    failures: list[SourceFailure] = []
    sources_queried: list[str] = []

    for index, papers, reason in raw_results:
        sources_queried.append(index)
        if papers is not None:
            all_papers.extend(papers)
        else:
            failures.append(SourceFailure(source=index, reason=reason or "unreachable"))

    deduped = deduplicate_paper_records(all_papers)
    total_found = len(deduped)
    truncated = total_found > max_results
    papers_out = deduped[:max_results]

    return SearchPapersResult(
        papers=papers_out,
        total_found=total_found,
        sources_queried=sources_queried,
        sources_failed=failures,
        truncated=truncated,
    )


