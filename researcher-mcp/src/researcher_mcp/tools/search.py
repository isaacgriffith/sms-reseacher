"""MCP search tools: fan-out search_papers, per-source search tools, and get_paper.

The upgraded ``search_papers`` fans out across all SourceRegistry-enabled sources
concurrently via :func:`asyncio.gather`, deduplicates results, and records any
partial failures in ``sources_failed``.

Per-source tools (``search_ieee``, ``search_scopus``, etc.) allow agents to
query individual databases directly.
"""

