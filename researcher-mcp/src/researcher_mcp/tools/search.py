@mcp.tool()
async def get_paper_semantic_scholar(
    doi: str | None = None,
    semantic_scholar_id: str | None = None,
) -> dict[str, Any]:
    """Retrieve detailed metadata for a single paper from Semantic Scholar.

    Args:
        doi: Paper DOI without the ``https://doi.org/`` prefix.
        semantic_scholar_id: Semantic Scholar paper ID (alternative to DOI).

    Returns:
        Dict with paper metadata including ``tldr``, ``influentialCitationCount``,
        ``isOpenAccess``, and ``fieldsOfStudy`` from the Semantic Scholar API.

    """
    ss, _, _ = _get_legacy_sources()
    paper_id = f"DOI:{doi}" if doi else (semantic_scholar_id or "")
    if not paper_id:
        return {"error": "Must provide either doi or semantic_scholar_id"}
    fields = [
        "title",
        "abstract",
        "externalIds",
        "year",
        "authors",
        "venue",
        "isOpenAccess",
        "fieldsOfStudy",
        "tldr",
        "influentialCitationCount",
    ]
    return await ss.get_paper(paper_id, fields)
