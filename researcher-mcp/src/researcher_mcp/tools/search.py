@mcp.tool()
async def search_ieee(
    query: str,
    max_results: int = 100,
    year_from: int | None = None,
    year_to: int | None = None,
    content_type: list[str] | None = None,
) -> list[PaperRecord]:
    """Search IEEE Xplore for papers matching *query*.

    Args:
        query: Free-text or boolean search string.
        max_results: Maximum results to return (up to 200).
        year_from: Earliest publication year filter.
        year_to: Latest publication year filter.
        content_type: Optional filter list (e.g. ``["Journals","Conference Publications"]``).

    Returns:
        List of :class:`PaperRecord` objects from IEEE Xplore.

    """
    registry = get_registry()
    source = registry.get("ieee_xplore")
    if source is None:
        return []
    return await source.search(query, max_results=max_results, year_from=year_from, year_to=year_to)


@mcp.tool()
async def get_ieee_paper(article_number: str) -> PaperRecord | None:
    """Retrieve a single IEEE Xplore paper by article number.

    Args:
        article_number: The IEEE-native article number identifier string.

    Returns:
        A :class:`PaperRecord` if found, or ``None``.

    """
