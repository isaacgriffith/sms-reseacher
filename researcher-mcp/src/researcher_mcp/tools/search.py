@mcp.tool()
async def search_acm(
    query: str,
    max_results: int = 100,
    year_from: int | None = None,
    year_to: int | None = None,
) -> dict[str, Any]:
    """Search the ACM Digital Library for papers matching *query*.

    Returns an ``AcmSearchResult``-shaped dict with ``papers`` and ``truncated``
    fields.  Rate limiting may result in fewer results than requested.

    Args:
        query: Keyword search query.
        max_results: Maximum records to request.
        year_from: Earliest publication year filter.
        year_to: Latest publication year filter.

    Returns:
        Dict with ``papers`` (list of :class:`PaperRecord`) and ``truncated`` bool.

    """
    registry = get_registry()
    source = registry.get("acm_dl")
    if source is None:
        return {"papers": [], "truncated": False}
    papers = await source.search(
        query, max_results=max_results, year_from=year_from, year_to=year_to
    )
    truncated = len(papers) == max_results
    return {"papers": [p.model_dump() for p in papers], "truncated": truncated}


